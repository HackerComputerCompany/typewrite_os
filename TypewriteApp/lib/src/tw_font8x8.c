const uint8_t *tw_font8x8_glyph(unsigned char c) {
    for (unsigned i = 0; i < sizeof(g_glyphs) / sizeof(g_glyphs[0]); i++) {
        if (g_glyphs[i].c == c)
            return g_glyphs[i].rows;
    }
    if (c >= 'a' && c <= 'z')
        return tw_font8x8_glyph((unsigned char)(c - 'a' + 'A'));
    return g_blank;
}

