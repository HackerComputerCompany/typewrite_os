Color invert_color(Color c) {
    return (Color){255 - c.r, 255 - c.g, 255 - c.b, c.a};
}

Color get_color(Color normal, Color dark) {
    if (doc.inverted) return invert_color(normal);
    return normal;
}

void draw_pixel(int x, int y, Color c) {
    if (x < 0 || x >= screen_width || y < 0 || y >= screen_height) return;
    unsigned long offset = (y * finfo.line_length) + (x * (vinfo.bits_per_pixel / 8));
    *(fbp + offset + 0) = c.b;
    *(fbp + offset + 1) = c.g;
    *(fbp + offset + 2) = c.r;
}

void draw_rect(int x, int y, int w, int h, Color c) {
    for (int dy = 0; dy < h; dy++) {
        for (int dx = 0; dx < w; dx++) {
            draw_pixel(x + dx, y + dy, c);
        }
    }
}

void draw_line_horizontal(int x1, int y, int x2, Color c) {
    for (int x = x1; x <= x2; x++) {
        draw_pixel(x, y, c);
    }
}

void draw_line_vertical(int x, int y1, int y2, Color c) {
    for (int y = y1; y <= y2; y++) {
        draw_pixel(x, y, c);
    }
}

int char_width(int scale) {
    return 8 * scale;
}

int char_height(int scale) {
    return 16 * scale;
}

int page_width(int scale) {
    return screen_width - doc.margin_left - doc.margin_right;
}

int chars_per_line(int scale) {
    return page_width(scale) / char_width(scale);
}

int page_height(int scale) {
    return screen_height - doc.margin_top - doc.ruler_height - 30;
}

int lines_per_page(int scale) {
    return page_height(scale) / char_height(scale);
}

int page_start_y() {
    return doc.margin_top + doc.ruler_height;
}

int page_start_x() {
    return doc.margin_left;
}

