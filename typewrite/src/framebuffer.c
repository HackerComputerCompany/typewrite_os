void draw_glyph(int x, int y, unsigned char ch, Color c, int scale) {
    int glyph_w = 8;
    int glyph_h = 16;
    int cw = char_width(scale);
    int ch_h = char_height(scale);
    
    int idx = ch - 32;
    if (idx < 0 || idx >= 95) idx = 0;
    
    for (int gy = 0; gy < glyph_h; gy++) {
        unsigned char row = font_8x16[idx][gy];
        for (int gx = 0; gx < glyph_w; gx++) {
            if (row & (0x80 >> gx)) {
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        draw_pixel(x + gx * scale + sx, y + gy * scale + sy, c);
                    }
                }
            }
        }
    }
}

void draw_text(int x, int y, const char *text, Color c, int scale) {
    int i = 0;
    while (text[i]) {
        draw_glyph(x + i * char_width(scale), y, text[i], c, scale);
        i++;
    }
}

void draw_ruler(int scale) {
    int cw = char_width(scale);
    int y = doc.margin_top;
    int end_x = screen_width - doc.margin_right;
    
    draw_rect(page_start_x(), y, end_x - page_start_x(), doc.ruler_height, get_color(COLOR_WHITE, COLOR_BLACK));
    draw_line_horizontal(page_start_x(), y + doc.ruler_height - 1, end_x, get_color(COLOR_BLACK, COLOR_WHITE));
    
    char buf[16];
    
    for (int x = page_start_x(), pos = 0; x < end_x && pos < 100; x += cw, pos++) {
        Color tick_color = get_color(COLOR_BLACK, COLOR_WHITE);
        if (pos % 10 == 0) {
            draw_rect(x, y + doc.ruler_height - 10, cw, 10, tick_color);
            snprintf(buf, sizeof(buf), "%d", pos);
            draw_text(x + 1, y + 2, buf, get_color(COLOR_WHITE, COLOR_BLACK), 1);
        } else if (pos % 5 == 0) {
            draw_rect(x, y + doc.ruler_height - 6, cw, 6, tick_color);
        } else {
            draw_rect(x, y + doc.ruler_height - 3, cw, 3, tick_color);
        }
    }
}

void draw_margins() {
    Color margin_color = get_color(COLOR_RED, invert_color(COLOR_RED));
    draw_line_vertical(doc.margin_left - 1, doc.margin_top, screen_height - 20, margin_color);
    draw_line_vertical(screen_width - doc.margin_right, doc.margin_top, screen_height - 20, margin_color);
}

void draw_page_background() {
    int start_y = page_start_y();
    int start_x = page_start_x();
    int w = screen_width - start_x - doc.margin_right;
    int h = screen_height - start_y - 20;
    draw_rect(start_x, start_y, w, h, get_color(COLOR_LIGHT_GRAY, invert_color(COLOR_LIGHT_GRAY)));
    draw_rect(start_x + 5, start_y + 5, w - 10, h - 10, get_color(COLOR_WHITE, COLOR_BLACK));
}

void draw_char(int line_idx, int char_idx, int scale) {
    int cw = char_width(scale);
    int ch_h = char_height(scale);
    int px = page_start_x() + 5 + char_idx * cw;
    int py = page_start_y() + 5 + (line_idx - doc.top_line) * ch_h;
    
    if (line_idx >= 0 && line_idx < doc.total_lines && char_idx < doc.lines[line_idx].char_count) {
        unsigned char ch_val = doc.lines[line_idx].text[char_idx];
        Color c = get_color(doc.ink_color, invert_color(doc.ink_color));
        draw_glyph(px, py, ch_val, c, scale);
        
        if (doc.lines[line_idx].strikethrough[char_idx]) {
            draw_rect(px, py + ch_h / 2 - 1, cw, 2, c);
        }
    }
}

void draw_status_bar() {
    int y = screen_height - 20;
    draw_rect(0, y, screen_width, 20, get_color(COLOR_WHITE, COLOR_BLACK));
    draw_line_horizontal(0, y, screen_width, get_color(COLOR_BLACK, COLOR_WHITE));
    
    char buf[256];
    const char *ink_names[] = {"Black", "Red", "Blue"};
    const char *mode_names[] = {"Light", "Dark"};
    int ink_idx = (doc.ink_color.r == 0 && doc.ink_color.b == 0) ? 0 : 
                  (doc.ink_color.r == 180) ? 1 : 2;
    int visible_chars = 0;
    for (int i = 0; i < doc.total_lines; i++) {
        visible_chars += doc.lines[i].char_count;
    }
    
    snprintf(buf, sizeof(buf), "Scale:%dx | %s | Ink:%s | Lines:%d | F1:color F2:smaller F3:larger F4:mode", 
             doc.scale, mode_names[doc.inverted], ink_names[ink_idx], doc.total_lines);
    draw_text(10, y + 4, buf, get_color(COLOR_BLACK, COLOR_WHITE), 1);
}

void render(int scale) {
    draw_rect(0, 0, screen_width, screen_height, get_color(COLOR_WHITE, COLOR_BLACK));
    draw_page_background();
    draw_ruler(scale);
    draw_margins();
    
    for (int i = doc.top_line; i < doc.total_lines && i - doc.top_line < lines_per_page(scale); i++) {
        for (int j = 0; j < doc.lines[i].char_count; j++) {
            draw_char(i, j, scale);
        }
    }
    
    int cursor_x = page_start_x() + 5 + doc.cursor_col * char_width(scale);
    int cursor_y = page_start_y() + 5 + (doc.cursor_line - doc.top_line) * char_height(scale);
    Color cursor_color = get_color(COLOR_BLACK, COLOR_WHITE);
    draw_rect(cursor_x, cursor_y, char_width(scale), char_height(scale), cursor_color);
    
    draw_status_bar();
}

void init_document() {
    memset(&doc, 0, sizeof(doc));
    doc.total_lines = 1;
    doc.ink_color = COLOR_BLACK;
    doc.scale = 2;
    doc.margin_left = 60;
    doc.margin_right = 40;
    doc.margin_top = 30;
    doc.ruler_height = 20;
    doc.inverted = 0;
}

int init_framebuffer() {
    fb_fd = open("/dev/fb0", O_RDWR);
    if (fb_fd == -1) {
        perror("Cannot open framebuffer");
        return -1;
    }
    
    if (ioctl(fb_fd, FBIOGET_VSCREENINFO, &vinfo) == -1) {
        perror("Error reading variable screen info");
        return -1;
    }
    
    if (ioctl(fb_fd, FBIOGET_FSCREENINFO, &finfo) == -1) {
        perror("Error reading fixed screen info");
        return -1;
    }
    
    screen_width = vinfo.xres;
    screen_height = vinfo.yres;
    
    fbp = (char *)mmap(0, finfo.smem_len, PROT_READ | PROT_WRITE, MAP_SHARED, fb_fd, 0);
    if (fbp == MAP_FAILED) {
        perror("Error mapping framebuffer");
        return -1;
    }
    
    return 0;
}

void close_framebuffer() {
    munmap(fbp, finfo.smem_len);
    close(fb_fd);
}

void insert_char(unsigned char c) {
    if (doc.cursor_line >= MAX_LINES) return;
    Line *line = &doc.lines[doc.cursor_line];
    if (line->char_count >= MAX_LINE_LEN - 1) return;
    
    for (int i = line->char_count; i > doc.cursor_col; i--) {
        line->text[i] = line->text[i - 1];
        line->strikethrough[i] = line->strikethrough[i - 1];
    }
    line->text[doc.cursor_col] = c;
    line->strikethrough[doc.cursor_col] = 0;
    line->char_count++;
    doc.cursor_col++;
}

void handle_backspace() {
    Line *line = &doc.lines[doc.cursor_line];
    
    if (doc.cursor_col > 0) {
        doc.cursor_col--;
        if (line->strikethrough[doc.cursor_col]) {
            for (int i = doc.cursor_col; i < line->char_count - 1; i++) {
                line->text[i] = line->text[i + 1];
                line->strikethrough[i] = line->strikethrough[i + 1];
            }
            line->char_count--;
        } else {
            line->strikethrough[doc.cursor_col] = 1;
        }
    } else if (doc.cursor_line > 0) {
        doc.cursor_line--;
        doc.cursor_col = doc.lines[doc.cursor_line].char_count;
        handle_backspace();
    }
}

void handle_enter() {
    if (doc.cursor_line >= MAX_LINES - 1) return;
    
    Line *current = &doc.lines[doc.cursor_line];
    Line *next = &doc.lines[doc.cursor_line + 1];
    
    for (int i = doc.cursor_col; i < current->char_count; i++) {
        next->text[i - doc.cursor_col] = current->text[i];
        next->strikethrough[i - doc.cursor_col] = current->strikethrough[i];
    }
    next->char_count = current->char_count - doc.cursor_col;
    
    for (int i = doc.cursor_col; i < current->char_count; i++) {
        current->text[i] = 0;
        current->strikethrough[i] = 0;
    }
    current->char_count = doc.cursor_col;
    
    doc.cursor_line++;
    doc.cursor_col = 0;
    
    if (doc.cursor_line >= doc.total_lines) {
        doc.total_lines = doc.cursor_line + 1;
    }
}

void word_wrap() {
    int cpl = chars_per_line(doc.scale);
    
    for (int line = 0; line < doc.total_lines; line++) {
        while (doc.lines[line].char_count > cpl) {
            int break_point = cpl;
            while (break_point > 0 && doc.lines[line].text[break_point] != ' ') {
                break_point--;
            }
            
            if (break_point == 0) break_point = cpl;
            
            int next_line = line + 1;
            if (next_line >= MAX_LINES) break;
            
            if (doc.lines[next_line].char_count == 0) {
                doc.total_lines = next_line + 1 > doc.total_lines ? next_line + 1 : doc.total_lines;
            }
            
            for (int i = break_point + 1; i < doc.lines[line].char_count; i++) {
                int dest = doc.lines[next_line].char_count++;
                if (dest < MAX_LINE_LEN - 1) {
                    doc.lines[next_line].text[dest] = doc.lines[line].text[i];
                    doc.lines[next_line].strikethrough[dest] = doc.lines[line].strikethrough[i];
                }
            }
            
            doc.lines[line].char_count = break_point;
            if (doc.lines[line].text[break_point] == ' ') {
                doc.lines[line].text[break_point] = 0;
                doc.lines[line].char_count = break_point;
            }
        }
    }
}

void move_cursor(int dx, int dy) {
    doc.cursor_col += dx;
    doc.cursor_line += dy;
    
    if (doc.cursor_col < 0 && doc.cursor_line > 0) {
        doc.cursor_line--;
        doc.cursor_col = doc.lines[doc.cursor_line].char_count;
    }
    if (doc.cursor_col < 0) doc.cursor_col = 0;
    
    if (doc.cursor_col > doc.lines[doc.cursor_line].char_count) {
        doc.cursor_col = doc.lines[doc.cursor_line].char_count;
    }
    
    if (doc.cursor_line < 0) doc.cursor_line = 0;
    if (doc.cursor_line >= doc.total_lines) {
        doc.cursor_line = doc.total_lines - 1;
        if (doc.cursor_line < 0) doc.cursor_line = 0;
    }
    
    while (doc.top_line > doc.cursor_line) doc.top_line--;
    int lpp = lines_per_page(doc.scale);
    while (doc.top_line + lpp <= doc.cursor_line) doc.top_line++;
    
    if (doc.cursor_line >= MAX_LINES) doc.cursor_line = MAX_LINES - 1;
    if (doc.total_lines < doc.cursor_line + 1) {
        doc.total_lines = doc.cursor_line + 1;
    }
}

void save_document() {
    FILE *f = fopen("/root/document.txt", "w");
    if (f) {
        for (int i = 0; i < doc.total_lines; i++) {
            for (int j = 0; j < doc.lines[i].char_count; j++) {
                if (!doc.lines[i].strikethrough[j]) {
                    fputc(doc.lines[i].text[j], f);
                }
            }
            if (i < doc.total_lines - 1) fputc('\n', f);
        }
        fclose(f);
    }
}

int main() {
    if (init_framebuffer() == -1) {
        fprintf(stderr, "Failed to initialize framebuffer\n");
        return 1;
    }
    
    struct termios old_tty, new_tty;
    tcgetattr(STDIN_FILENO, &old_tty);
    new_tty = old_tty;
    new_tty.c_lflag &= ~(ICANON | ECHO);
    new_tty.c_cc[VMIN] = 0;
    new_tty.c_cc[VTIME] = 0;
    tcsetattr(STDIN_FILENO, TCSAFLUSH, &new_tty);
    
    init_document();
    render(doc.scale);
    
    fd_set readfds;
    int running = 1;
    
    while (running) {
        FD_ZERO(&readfds);
        FD_SET(STDIN_FILENO, &readfds);
        struct timeval tv = {0, 100000};
        
        if (select(STDIN_FILENO + 1, &readfds, NULL, NULL, &tv) > 0) {
            unsigned char c;
            if (read(STDIN_FILENO, &c, 1) > 0) {
                int handled = 1;
                
                if (c == 27) {
                    unsigned char next;
                    if (read(STDIN_FILENO, &next, 1) > 0 && next == '[') {
                        read(STDIN_FILENO, &next, 1);
                        switch (next) {
                            case 'A': move_cursor(0, -1); break;
                            case 'B': move_cursor(0, 1); break;
                            case 'C': move_cursor(1, 0); break;
                            case 'D': move_cursor(-1, 0); break;
                        }
                    }
                } else if (c == 17 || c == 3) {
                    running = 0;
                } else if (c == 19) {
                    save_document();
                } else if (c == 127 || c == 8) {
                    handle_backspace();
                } else if (c == '\n' || c == '\r') {
                    handle_enter();
                    word_wrap();
                } else if (c == KEY_F1) {
                    if (doc.ink_color.r == 0 && doc.ink_color.b == 0) doc.ink_color = COLOR_RED;
                    else if (doc.ink_color.r == 180) doc.ink_color = COLOR_BLUE;
                    else doc.ink_color = COLOR_BLACK;
                } else if (c == KEY_F2) {
                    if (doc.scale > 1) doc.scale--;
                } else if (c == KEY_F3) {
                    if (doc.scale < 4) doc.scale++;
                } else if (c == KEY_F4) {
                    doc.inverted = !doc.inverted;
                } else if (c >= 32 && c <= 126) {
                    insert_char(c);
                    word_wrap();
                } else {
                    handled = 0;
                }
                
                if (running) render(doc.scale);
            }
        }
    }
    
    tcsetattr(STDIN_FILENO, TCSAFLUSH, &old_tty);
    close_framebuffer();
    
    return 0;
}
